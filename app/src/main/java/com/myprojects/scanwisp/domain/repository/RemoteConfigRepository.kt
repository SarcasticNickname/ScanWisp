package com.myprojects.scanwisp.domain.repository

/**
 * Репозиторий для получения значений из удаленной конфигурации (Remote Config).
 * Абстрагирует ViewModel от конкретной реализации (Firebase), что упрощает тестирование
 * и возможную замену провайдера в будущем.
 */
interface RemoteConfigRepository {

    /**
     * Проверяет, включен ли показ нативной рекламы.
     * @return `true`, если реклама включена, иначе `false`.
     */
    fun isNativeAdEnabled(): Boolean

    /**
     * Получает позицию, после которой может появиться первое рекламное объявление.
     * @return Целое число, представляющее позицию.
     */
    fun getNativeAdStartPosition(): Int

    /**
     * Получает интервал (количество элементов контента), через который должны отображаться
     * последующие рекламные блоки.
     * @return Целое число, представляющее интервал.
     */
    fun getNativeAdInterval(): Int

    /**
     * Запускает процесс получения и активации последних значений из Remote Config.
     * Должен вызываться при старте приложения, чтобы обновить кешированные значения.
     */
    fun fetchAndActivate()
}