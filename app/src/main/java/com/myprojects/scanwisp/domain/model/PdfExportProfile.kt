package com.myprojects.scanwisp.domain.model

/**
 * Перечисление, представляющее различные профили качества для экспорта в PDF.
 */
enum class PdfExportProfile {
    /** Сильное сжатие, низкое разрешение, всегда ч/б. Для минимального размера файла. */
    SMALL,

    /** Умеренное сжатие, среднее разрешение. Оптимальный баланс. */
    BALANCED,

    /** Без дополнительного сжатия и изменения разрешения. Максимальное качество. */
    HIGH
}